def compute_cost_logistic(X_train_s, y_train, w, b):
    cost = 0.0
    z = X_train_s @ w + b
    y_hat = sigmoid(z)
    eps = 1e-15
    y_hat = np.clip(y_hat, eps, 1-eps)
    cost = -np.mean(y_train * np.log(y_hat) + (1 - y_train) * np.log(1 - y_hat))
    return cost

