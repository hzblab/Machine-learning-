def gradient(X_train_s, y_train, w, b):
  m = X_train_s.shape[0]
  z = X_train_s @ w + b
  y_hat = sigmoid(z)
  error = y_hat - y_train
  dw = 1/m * X_train_s.T @ error
  db = 1/m * np.sum(error)
  return dw, db

